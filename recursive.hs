import Prelude hiding (concat, (++), drop, zip, reverse, length, product)
{-# HLINT ignore "Use foldr" #-}
fac 0 = 1
fac n = n * fac(n-1)

product :: Num a => [a] -> a
product [] = 1
product (n:ns) = n * product ns

length :: [a] -> Int
length [] = 1
length (_:ns) = 1 + length(ns)

reverse :: [a] -> [a]
reverse [] = []
reverse (x:xs) = reverse xs ++ [x]

zip :: [a] -> [b] -> [(a,b)]
zip []  _ = []
zip _  [] = []
zip (x:xs) (y:ys) = (x,y) : zip xs ys

drop :: Int -> [a] -> [a]
drop 0 xs = xs
drop _ [] = []
drop n (_:xs) = drop (n-1) xs

(++) :: [a] -> [a] -> [a]
[] ++ ys = ys
(x:xs) ++ ys = x : (xs ++ ys)

-- QUICKSORT ALGORITHM

qsort :: Ord a => [a] -> [a]
qsort [] = []
qsort(x:xs) = 
  qsort smaller ++ [x] ++ qsort larger
  where
    smaller = [a | a <- xs, a <= x]
    larger = [b | b <- xs, b > x]

concat :: [[a]] -> [a]
concat [] = []
concat (x:xs) = x ++ concat xs
